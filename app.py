def add(a, b):
    return a + b


def sub(a, b):
    return a - b


def mul(a, b):
    return a * b


if __name__ == "__main__":  
    result1 = add(10, 20)
    result2 = sub(20, 10)
    result3 = mul(10, 20)

    print("Addition Result:", result1)
    print("Subtraction Result:", result2)
    print("Multiplication Result:", result3)
